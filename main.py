def delete_user(user_id):
    from database import DB_NAME
    with sqlite3.connect(DB_NAME) as conn:
        cursor = conn.cursor()
        cursor.execute("DELETE FROM users WHERE user_id = ?", (user_id,))
        cursor.execute("DELETE FROM workouts WHERE user_id = ?", (user_id,))
        conn.commit()
    return True

